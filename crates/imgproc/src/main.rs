use imgproc::*;

fn main() {
    let a = [
        0x0, 0x1, 0x2, 0x3, 0x4, 0x5, 0x6, 0x7, 0x8, 0x9, 0xA, 0xB, 0xC, 0xD, 0xE, 0xF,
    ];
    let b = [
        0xF, 0xE, 0xD, 0xC, 0xB, 0xA, 0x9, 0x8, 0x7, 0x6, 0x5, 0x4, 0x3, 0x2, 0x1, 0x0,
    ];

    let brightest = unsafe {
        let mut dst = [0u8; 16];
        brightest(a.as_ptr(), b.as_ptr(), dst.as_mut_ptr(), a.len());
        dst
    };

    for i in brightest {
        print!("{} ", i);
    }
}
